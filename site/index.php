<h2><a name="intro" id="introduction"></a>Introduction</h2>

<p class="abstract">A feature is a semantically
cohesive unit of behavior of a software system. Features are used for
the specification of commonalities and variabilities of software
systems. For example, in the domain of data management there are
features such as transaction management, storage management, and
multi-user support. <br />

<br />

It would be easier to develop software if its features would be
explicit in design and code and if software could be generated solely
by composing features. This methodology is called <a href="http://en.wikipedia.org/wiki/Feature_Oriented_Programming">Feature-Oriented
Software Development</a> (FOSD). Currently, there is a multitude
of different methods, languages, and tools for FOSD that -- despite
their common aim -- are difficult to compare and integrate. For a recent survey see the paper <a  target="_blank" href="http://www.jot.fm/issues/issue_2009_07/column5/index.html">An Overview of Feature-Oriented Software Development</a>.</p>

<p class="abstract">This web site acts as a
portal for different projects in FOSD research.
</p>

